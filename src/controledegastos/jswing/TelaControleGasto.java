    private void btnAddGastoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddGastoActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tableFrame.getSelectedRow();
        Long cpf = (Long) tableFrame.getValueAt(linhaSelecionada, 1);
        Usuario usuario = this.iUsuario.buscarPorCpf(cpf);

        String descricao = JOptionPane.showInputDialog(null, "Descrição do seu gasto:", "Adicionar gasto", JOptionPane.INFORMATION_MESSAGE);
        String valor = JOptionPane.showInputDialog(null, "Valor do seu gasto:", "Adicionar gasto", JOptionPane.INFORMATION_MESSAGE);
        Gasto gasto = new Gasto(descricao, Integer.valueOf(valor));

        this.iUsuario.addGasto(usuario, gasto);
    }//GEN-LAST:event_btnAddGastoActionPerformed

    private void btnVerGastosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerGastosActionPerformed
        // TODO add your handling code here:
        int linhaSelecionada = tableFrame.getSelectedRow();
        Long cpf = (Long) tableFrame.getValueAt(linhaSelecionada, 1);

        Usuario usuario = this.iUsuario.buscarPorCpf(cpf);

        JOptionPane.showMessageDialog(null, usuario.getGastos(), "Usuário: " + usuario.getNome(), JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnVerGastosActionPerformed

