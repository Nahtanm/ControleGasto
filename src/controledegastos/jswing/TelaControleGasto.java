    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String nome = nomeTxt.getText();
        String cpf = cpfTxt.getText();
        String salario = salarioTxt.getText();

        Usuario usuario = new Usuario(nome, Integer.valueOf(salario), cpf);

        if (iUsuario.cadastrarUsuario(usuario) != null) {
            defaultTableModel.addRow(new Object[]{usuario.getNome(), usuario.getCpf(), usuario.getSalario(), usuario.getSaldo()});
            iUsuario.cadastrarUsuario(usuario);
            limparCampos();
            JOptionPane.showMessageDialog(null, "Usuário cadastrado!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(null, "Usuário já existe!", "Cadastro", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_btnCadastrarActionPerformed

